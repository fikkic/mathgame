def write_map(mapi):
    from player.player import player
    from entities.enemy import spawn_enemy
    for y in range(len(mapi)):
        for x in range(len(mapi[y])):
            position = (x * sell_size[0] * scale_x, y * sell_size[1] * scale_y)
            if mapi[y][x] == 1:
                collide_tiles.add(Tile(position[0], position[1], "wall",
                                       (sell_size[0] * scale_x, sell_size[1] * scale_y)))
            if mapi[y][x] == 6: spawn_enemy("plus", position)
            if mapi[y][x] == 7: spawn_enemy("minus", position)
            if mapi[y][x] == 8: #хилки спавн
                item_group.add(Item(position, item_types["heal"], 0.4))
            if mapi[y][x] == 9: #портал (выход с уровня)
                tiles.add(Tile(position[0], position[1], "portal",
                                       (sell_size[0] * scale_x, sell_size[1] * scale_y)))
            if mapi[y][x] == 10:
                tiles.add(Item(position, item_types["cup"], 0.1))
            if mapi[y][x] == 2:
                player.rect.midbottom = position

            map_cells.append(Cell(x * sell_size[0] * scale_x, y * sell_size[1] * scale_y))

